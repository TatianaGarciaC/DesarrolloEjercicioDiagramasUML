package universidadean.ejercicio3;

public class Fecha {
	
	Integer anio;
	Integer mes;
	Integer segundodia;
	
	public Integer getAnio() {
		return anio;
	}
	public void setAnio(Integer anio) {
		this.anio = anio;
	}
	public Integer getMes() {
		return mes;
	}
	public void setMes(Integer mes) {
		this.mes = mes;
	}
	public Integer getSegundodia() {
		return segundodia;
	}
	public void setSegundodia(Integer segundodia) {
		this.segundodia = segundodia;
	}
	
	
}
